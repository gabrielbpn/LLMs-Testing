# This Python code solves the problem of dividing a watermelon into two even-weight parts.

# Input: Weight of the watermelon in kilos.
w = int(input())

# Check if the weight is even.
if w % 2 == 0:
    # Print YES if the watermelon can be divided into two even-weight parts.
    print("YES")
else:
    # Print NO if the watermelon cannot be divided into two even-weight parts.
    print("NO")

